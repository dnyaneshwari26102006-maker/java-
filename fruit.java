public class fruit {
    public static void main(String[] args) {
        String fruit = "Apple";
        switch (fruit) {
            case "Apple":
                System.out.println("it is an apple");
                break;

            case "Banana":
                System.out.println("it is a banana");
                break;

            default:
                System.out.println("unknown fruit");
        }
    }
}
